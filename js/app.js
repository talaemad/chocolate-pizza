var check1 = document.getElementById('check1');
var label1 = document.getElementById('label1');
var newLabel1 = document.createElement('del');
var data1 = label1.textContent;
newLabel1.textContent = data1;
check1.onclick = function () {
    if (this.checked) {
        label1.textContent = '';
        label1.appendChild(newLabel1);
    }
    else{
        label1.textContent=data1;
    }
}

